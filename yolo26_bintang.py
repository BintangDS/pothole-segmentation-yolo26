import cv2
from ultralytics import YOLO
import numpy as np
from google.colab.patches import cv2_imshow
from google.colab import files
import os

# ==========================================
# KONFIGURASI ESTETIKA IDENTIK YOLO (HIJAU)
# ==========================================
# Warna "Hijau Sehat" bawaan pabrik (BGR)
WARNA_HIJAU_YOLO = (56, 219, 96) # Hijau terang khas Ultralytics tapi sehat
WARNA_TEKS_PUTIH = (255, 255, 255)
KETEBALAN_GARIS = 2
UKURAN_FONT = 0.5            # Standar minimalis YOLO
KETEBALAN_FONT = 1
# TRANSPARANSI MASKER (Agar lubang tetap terlihat sedikit)
# Gunakan 0.3 untuk tipis, 0.5 untuk sedang (seperti videomu dulu)
TINGKAT_TRANSPARANSI = 0.4
# ==========================================

# 1. Muat Model AI YOLO26 Kamu
model_path = '/content/drive/MyDrive/Penelitian_YOLO26/Run_2_Pothole_Campuran/weights/best.pt'
if not os.path.exists(model_path):
    print(f"❌ Error: Model tidak ditemukan di Drive! Pastikan path benar: {model_path}")
    exit()

model = YOLO(model_path)

# 2. Siapkan Gambar Sumber
# Silakan upload foto lubang dan ubah nama jadi 'pothole_test.jpg' di folder content
image_path = '/content/pothole_test.jpg'
img = cv2.imread(image_path)

if img is None:
    print("❌ Error: Gambar 'pothole_test.jpg' tidak ditemukan di folder content! Silakan upload dulu.")
else:
    print("🎬 Memproses gambar dengan gaya visual IDENTIK YOLO Hijau...")

    # Siapkan overlay untuk masker transparan
    img_final = img.copy()
    mask_overlay = np.zeros_like(img, dtype=np.uint8)

    # 3. Jalankan Prediksi (Menggunakan mode .predict untuk gambar statis)
    # verbose=False agar layar tidak penuh teks
    results = model.predict(img, verbose=False, conf=0.3)

    # Pastikan ada objek segmen poligon yang terdeteksi
    if results[0].boxes is not None and results[0].masks is not None:
        boxes = results[0].boxes.xyxy.cpu().numpy()
        confs = results[0].boxes.conf.cpu().numpy()
        clss = results[0].boxes.cls.cpu().numpy().astype(int)
        masks = results[0].masks.xy # Koordinat poligon

        class_names = model.names

        # Iterasi setiap objek lubang di dalam foto
        for box, conf, cls_idx, mask_polygons in zip(boxes, confs, clss, masks):
            x1, y1, x2, y2 = map(int, box)

            # --- A. GAMBAR BOUNDING BOX HIJAU TEBAL ---
            cv2.rectangle(img_final, (x1, y1), (x2, y2), WARNA_HIJAU_YOLO, KETEBALAN_GARIS)

            # --- B. GAMBAR ISIAN MASKER SEMI-TRANSPARAN (FILLED MASK) ---
            if len(mask_polygons) > 0:
                polygon_coords = mask_polygons.astype(np.int32)
                # Gambar poligon hijau penuh di mask_overlay
                cv2.fillPoly(mask_overlay, [polygon_coords], WARNA_HIJAU_YOLO)
                # Gambar garis tepi poligon agar tajam (seperti videomu dulu)
                cv2.polylines(img_final, [polygon_coords], True, WARNA_HIJAU_YOLO, KETEBALAN_GARIS)

            # --- C. RACIK TEKS SEPERTI BAWAAN YOLO (KELAS + CONF) ---
            orig_cls_name = class_names[cls_idx]
            label = f"{orig_cls_name} {conf:.2f}" # Contoh: severity-1 0.55

            # --- D. BUAT KOTAK BACKGROUND LABEL HIJAU TEBAL ---
            # Hitung ukuran teks
            (w, h), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, UKURAN_FONT, KETEBALAN_FONT)

            # Tentukan posisi kotak label (di atas kotak deteksi)
            # Pastikan tidak keluar layar atas
            label_y = y1
            if label_y < h + 10: label_y = h + 10

            # Gambar kotak latar belakang hijau penuh (-1)
            cv2.rectangle(img_final, (x1, label_y - h - 8), (x1 + w + 4, label_y), WARNA_HIJAU_YOLO, -1)

            # --- E. TULIS TEKS PUTIH DI ATAS KOTAK HIJAU ---
            cv2.putText(img_final, label, (x1 + 2, label_y - 5),
                        cv2.FONT_HERSHEY_SIMPLEX, UKURAN_FONT, WARNA_TEKS_PUTIH,
                        KETEBALAN_FONT, cv2.LINE_AA)

    # 4. Gabungkan Masker Transparan dengan Gambar Utama
    # Ini yang membuat isian warna hijau jadi tembus pandang
    cv2.addWeighted(mask_overlay, TINGKAT_TRANSPARANSI, img_final, 1, 0, img_final)

    # 5. Tampilkan Hasil Langsung di Colab
    print("\n📸 HASIL DETEKSI AI (GAYA KUSTOM IDENTIK YOLO HIJAU):")
    cv2_imshow(img_final)

    # 6. Simpan Gambar Hasil
    output_path = '/content/hasil_deteksi_gambar_HIJAU.jpg'
    cv2.imwrite(output_path, img_final)
    print(f"\n✅ Gambar hasil disimpan di: {output_path}")

    # 7. Unduh Otomatis
    print("📥 Menyiapkan unduhan otomatis...")
    files.download(output_path)

