import cv2
from ultralytics import YOLO
import numpy as np
import os
from google.colab import files

# ==========================================
# KONFIGURASI WARNA YOLOV8 (KUSTOM)
# ==========================================
WARNA_HIJAU = (0, 200, 0)     # Hijau sehat (BGR) untuk Box, Label, dan Poligon
WARNA_TEKS = (255, 255, 255)  # Putih (seperti bawaan YOLO)
KETEBALAN_GARIS = 2
UKURAN_FONT = 0.5             # Ukuran standar YOLO
KETEBALAN_FONT = 1
# ==========================================

# 1. Muat Model AI Kamu (Run_2)
model_path = '/content/drive/MyDrive/Penelitian_YOLO26/Run_2_Pothole_Campuran/weights/best.pt'
model = YOLO(model_path)

# 2. Buka Video Sumber
video_path = '/content/p.mp4'
cap = cv2.VideoCapture(video_path)

if not cap.isOpened():
    print("❌ Error: Gagal membuka video.")
    exit()

# Dapatkan properti video
frame_width = int(cap.get(3))
frame_height = int(cap.get(4))
fps = cap.get(cv2.CAP_PROP_FPS)

# 3. Siapkan Video Writer
output_path = '/content/sample_video_HIJAU_YOLO.mp4'
fourcc = cv2.VideoWriter_fourcc(*'mp4v')
out = cv2.VideoWriter(output_path, fourcc, fps, (frame_width, frame_height))

print("🎬 Memproses video dengan gaya YOLO Hijau (Tanpa Masker)...")

while cap.isOpened():
    ret, frame = cap.read()
    if not ret:
        break

    # Jalankan Tracking
    results = model.track(frame, persist=True, verbose=False, conf=0.3)

    if results[0].boxes.id is not None and results[0].masks is not None:
        boxes = results[0].boxes.xyxy.cpu().numpy()
        ids = results[0].boxes.id.cpu().numpy().astype(int)
        confs = results[0].boxes.conf.cpu().numpy()
        clss = results[0].boxes.cls.cpu().numpy().astype(int)
        masks = results[0].masks.xy

        class_names = model.names

        for box, id_num, conf, cls_idx, mask_polygons in zip(boxes, ids, confs, clss, masks):
            x1, y1, x2, y2 = map(int, box)

            # --- A. GAMBAR BOUNDING BOX HIJAU ---
            cv2.rectangle(frame, (x1, y1), (x2, y2), WARNA_HIJAU, KETEBALAN_GARIS)

            # --- B. GAMBAR GARIS POLIGON HIJAU (TANPA MASKER ISI) ---
            if len(mask_polygons) > 0:
                polygon_coords = mask_polygons.astype(np.int32)
                cv2.polylines(frame, [polygon_coords], True, WARNA_HIJAU, KETEBALAN_GARIS)

            # --- C. RACIK TEKS SEPERTI BAWAAN YOLO ---
            # Format persis seperti gambar biru: "id:XX severity-1 0.XX"
            label = f"id:{id_num} {class_names[cls_idx]} {conf:.2f}"

            # --- D. BUAT KOTAK BACKGROUND LABEL ---
            # Hitung ukuran teks untuk membuat kotak latar belakang yang pas
            (w, h), _ = cv2.getTextSize(label, cv2.FONT_HERSHEY_SIMPLEX, UKURAN_FONT, KETEBALAN_FONT)

            # Gambar kotak filled (-1) sebagai background teks
            cv2.rectangle(frame, (x1, y1 - h - 8), (x1 + w + 2, y1), WARNA_HIJAU, -1)

            # --- E. TULIS TEKS PUTIH DI ATAS KOTAK HIJAU ---
            cv2.putText(frame, label, (x1 + 1, y1 - 4),
                        cv2.FONT_HERSHEY_SIMPLEX, UKURAN_FONT, WARNA_TEKS,
                        KETEBALAN_FONT, cv2.LINE_AA)

    # Simpan frame
    out.write(frame)

cap.release()
out.release()
print("✅ Pemrosesan selesai!")

